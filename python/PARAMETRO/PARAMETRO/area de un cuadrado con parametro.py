# 1 DISEÑA UNA APLICACION QUE CALCULE EL AREA DE UN CUADRADO Y LUEGO SEA LLAMADA POR UN ALGORITMOS

#DEFINIR LA FUNCION
def area(l):
    a=(l*l)*2
    print("El area del cuadrado es de: ",a , "cm")

#APP QUE CALCULE EL AREA DEL CUADRADO
l=int(input("Ingrese la l del cuadrado: "))

#LLAMANDO LA FUNCIÓN
area(l)
