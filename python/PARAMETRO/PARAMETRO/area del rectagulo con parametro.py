#DEFINIR LA FUNCION
def area(b,h):
    a=b*h
    print("El area del rectangulo es de: ",a , "cm")

#APP QUE CALCULE EL AREA DEL RECTANGULO
b=int(input("Ingrese la b del rectangulo: "))
h=int(input("Ingrese la h del rectangulo: "))


#LLAMANDO LA FUNCIÓN
area(b,h)
